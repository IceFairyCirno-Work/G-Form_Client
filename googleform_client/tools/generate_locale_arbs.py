def build_locale(locale: str, index: int) -> dict:  # index: 0=ja, 1=zh
    out = {"@@locale": locale}
    for key, value in en_data.items():
        if key.startswith("@@"):
            continue
        if key.startswith("@"):
            out[key] = value
            continue
        if key in T:
            out[key] = T[key][index]
        else:
            raise KeyError(f"Missing translation for {key}")
    return out


def write_arb(path: Path, data: dict) -> None:
    path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


